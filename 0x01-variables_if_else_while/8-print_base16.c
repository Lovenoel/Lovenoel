#include <stdio.h>
#include <stdio.h>
#include <time.h>

/**
 * main - Prints all the numbers of base 16 in the lowercase
 *
 * Return: Always 0 (success)
 */
int main(void)
{
	char h;

	char q;

	h = 'a';
	while
		(q < 10) {
			putchar(q + '0');
			q++;
		}
	while
		(h <= 'f') {
			putchar(h);
			h++;
		}
	putchar('\n');
	return (0);
}
